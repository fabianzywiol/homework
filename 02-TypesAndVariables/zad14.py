# Determine radius and PI
radius = 5
PI = 3.14159

# Calculate area
area = PI * (radius ** 2)

# Calculate circumference
circumference = 2 * PI * radius

# Display results 
print(f"Radius of the circle: {radius}")
print(f"Area of the circle: {area}") 
print(f"Circumference of the circle: {circumference}")
